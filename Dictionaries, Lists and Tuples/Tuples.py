names = ('Carl','Aero','Neightan', 'Ayo', 'Ahmed', 'Abi')

teams = (1, 'Middlesbrough', 2, 'Leeds', 3, 'Newcastle', 4, 'Sunderland')
#tuples can take data that has different types.


#prints first element
print(names[0])
print('')

#prints last element
print(names[-1])
print('')


#prints first element with integer key
print(teams[0], '.', teams[1])
print('')

#prints second to third element
print(names[1:3])
print('')

#prints elements from beginning to end
print(teams[:])

